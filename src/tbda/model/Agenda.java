package tbda.model;

public class Agenda {
	int nagenda;
	String dia;
	int codm;
	int hora_inicio;
	int no_doentes;
	public int getNagenda() {
		return nagenda;
	}
	public void setNagenda(int nagenda) {
		this.nagenda = nagenda;
	}
	public String getDia() {
		return dia;
	}
	public void setDia(String dia) {
		this.dia = dia;
	}
	public int getCodm() {
		return codm;
	}
	public void setCodm(int codm) {
		this.codm = codm;
	}
	public int getHora_inicio() {
		return hora_inicio;
	}
	public void setHora_inicio(int hora_inicio) {
		this.hora_inicio = hora_inicio;
	}
	public int getNo_doentes() {
		return no_doentes;
	}
	public void setNo_doentes(int no_doentes) {
		this.no_doentes = no_doentes;
	}
}
